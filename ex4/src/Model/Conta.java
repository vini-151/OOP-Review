package Model;

public class Conta {
    private int numero;
    private String titular;

    public int getNumero() {
        return numero;
    }

    public String getTitular() {
        return titular;
    }

    public void setNumero(int numero) {
        this.numero = numero;
    }

    public void setTitular(String titular) {
        this.titular = titular;
    }
}
